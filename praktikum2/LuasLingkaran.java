public class LuasLingkaran {

    public static void main(String[] args) {
        double phi = 3.14;
        int r = 5;
        double luas = phi*r*r;
        System.out.println("\t Luas Lingkaran\t");
        System.out.println("phi = " + phi);
        System.out.println("r = " + r);
        System.out.println("luas = " + luas);
    }
}